// ─── Status counts helper ─────────────────────────────────────────────────────

export function computeMetrics(docs: Document[]) {
    return {
        total: docs.length,
        aprobados: docs.filter((d) => d.status === 'aprobado').length,
        pendientes: docs.filter((d) => d.status === 'pendiente').length,
        revisar: docs.filter((d) => d.status === 'revisar').length,
        duplicados: docs.filter((d) => d.status === 'duplicado').length,
        observados: docs.filter((d) => d.status === 'observado').length,
        montoTotal: docs.reduce((sum, d) => sum + d.total, 0),
        documentosHoy: docs.filter((d) => {
            const today = new Date().toISOString().split('T')[0];
            return d.uploadedAt.startsWith(today);
        }).length,
    };
}

